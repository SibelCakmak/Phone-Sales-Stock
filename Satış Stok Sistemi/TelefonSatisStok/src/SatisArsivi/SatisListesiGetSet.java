package SatisArsivi;

public class SatisListesiGetSet {
	
	String urunAdi;
	String kategorisi;
	int adedi;
	String hafiza;
	String satisFiyati;
	String tarih;
	int id;
	
	public SatisListesiGetSet() {
	
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getUrunAdi() {
		return urunAdi;
	}

	public void setUrunAdi(String urunAdi) {
		this.urunAdi = urunAdi;
	}

	public String getKategorisi() {
		return kategorisi;
	}

	public void setKategorisi(String kategorisi) {
		this.kategorisi = kategorisi;
	}

	public int getAdedi() {
		return adedi;
	}

	public void setAdedi(int adedi) {
		this.adedi = adedi;
	}

	public String getHafiza() {
		return hafiza;
	}

	public void setHafiza(String hafiza) {
		this.hafiza = hafiza;
	}

	public String getSatisFiyati() {
		return satisFiyati;
	}

	public void setSatisFiyati(String satisFiyati) {
		this.satisFiyati = satisFiyati;
	}

	public String getTarih() {
		return tarih;
	}

	public void setTarih(String tarih) {
		this.tarih = tarih;
	}
	

}
